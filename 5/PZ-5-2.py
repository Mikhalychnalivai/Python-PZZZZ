# Задача 2: Описать функцию AddRightDigit(D, K), добавляющую к целому положительному числу K справа цифру D (D — входной параметр целого типа, лежащий в диапазоне 0-9, K — параметр целого типа, являющийся одновременно входным и выходным). С помощью этой функции последовательно добавить к данному числу K справа данные цифры D1 и D2, выводя результат каждого добавления.
def AddRightDigit(D, K):
    K = K * 10 + D
    return K

K = int(input("Введите начальное число K: "))
D1 = int(input("Введите первую цифру D1 (0-9): "))
D2 = int(input("Введите вторую цифру D2 (0-9): "))

K = AddRightDigit(D1, K)
print(f"После добавления D1={D1}: {K}")

K = AddRightDigit(D2, K)
print(f"После добавления D2={D2}: {K}")